def get_hexagram(lines):
    """根据六爻获取卦象信息，lines为从下到上6爻列表，6=老阴,7=少阳,8=少阴,9=老阳"""
    # 转换为0/1，6,8为阴(0)，7,9为阳(1)
    base = tuple(1 if l in (7, 9) else 0 for l in lines)
    # 变爻后的卦，6->1, 9->0
    changed = tuple(
        (1 if l == 6 else 0) if l in (6, 9) else base[i]
        for i, l in enumerate(lines)
    )
    main = HEXAGRAMS.get(base)
    alt = HEXAGRAMS.get(changed) if changed != base else None
    return main, alt, base, changed
